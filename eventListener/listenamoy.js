// Connect to the Polygon network (or Ethereum) using an RPC provider
const provider = new ethers.providers.JsonRpcProvider("https://rpc.ankr.com/polygon_amoy"); // You can replace this with your preferred RPC provider

// Create a contract instance
const contract = new ethers.Contract(contractAddress, abi, provider);

// Check if the contract is connected
async function checkContract() {
    try {
      const name = await contract.name();
      console.log(`Connected to contract: ${name}`);
    } catch (error) {
      console.error("Error connecting to contract:", error);
    }
  }
  
  // Run the check
  checkContract();

// Listen for the 'assetLocked' event
contract.on("assetLocked", (uid, owner, amount, event) => {
  console.log(`Asset Locked Event Detected:`);
  console.log(`UID: ${uid}`);
  console.log(`Owner: ${owner}`);
  console.log(`Amount: ${ethers.utils.formatUnits(amount, 18)} tokens`);
  console.log(`Transaction Hash: ${event.transactionHash}`);
  console.log(`Block Number: ${event.blockNumber}`);
});

console.log("Listening for assetLocked events...");

